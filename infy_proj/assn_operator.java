package infy_proj;

public class assn_operator {
    public static void main(String[] args) {
        int num = 50;
        System.out.println(num);

        num += 5;
        System.out.println(num);

        num -= 5;
        System.out.println(num);

        num *= 5;
        System.out.println(num);

        num /= 5;
        System.out.println(num);

    }

}
